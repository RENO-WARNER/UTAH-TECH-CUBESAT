mod Database {
	use std::io;
	use std::fs::File;
	use std::io::BufRead;

	static FILENAME: &str = "./catalog/bsc5.dat";

	#[derive(Debug)]
	pub struct Star {
		pub id: u16,
		pub right_ascension: f32,
		pub declination: f32,
		vmag: f32,
	}

	impl Star {
		pub fn angle(a: &Star, b: &Star) -> f32 {
			(a.declination.sin() * b.declination.sin()
				+ a.declination.cos() * b.declination.cos() * (a.right_ascension - b.right_ascension).cos())
				.acos()
		}
	}
    fn parse_star_line(line: &str) -> Option<Star> {
		use std::str::FromStr;

		let id = u16::from_str(&line[0..4].trim()).ok()?;

		// Right Ascension
		let ra_h = u8::from_str(&line[75..77].trim()).ok()? as f32;
		let ra_m = u8::from_str(&line[77..79].trim()).ok()? as f32;
		let ra_s = f32::from_str(&line[79..83].trim()).ok()?;
		let ra = (ra_h * 15.0 + ra_m / 4.0 + ra_s / 240.0).to_radians();
	
		// Declination
		let dec_sign = if &line[83..84] == "+" { 1.0 } else { -1.0 };
		let dec_d = u8::from_str(&line[84..86].trim()).ok()? as f32;
		let dec_m = u8::from_str(&line[86..88].trim()).ok()? as f32;
		let dec_s = u8::from_str(&line[88..90].trim()).ok()? as f32;
		let dec = dec_sign * (dec_d + dec_m / 60.0 + dec_s / 3600.0).to_radians();
	
		// Visual Magnitude
		let vmag = f32::from_str(&line[102..107].trim()).ok()?;

		return Some(Star {
			id,
			right_ascension: ra,
			declination: dec,
			vmag,
		});
	}
	fn read_stars_from_file(filename: &str) -> io::Result<Vec<Star>> {
		let mut stars = Vec::new();
	
		let file = File::open(filename)?;
		for line in io::BufReader::new(file).lines() {
			if let Ok(line) = line {
				if let Some(star) = parse_star_line(&line) {
					stars.push(star);
				}
			}
		}

		stars.sort_by(|a, b| b.vmag.partial_cmp(&a.vmag).unwrap());

		return Ok(stars)
	}
	pub fn generate_stars() -> io::Result<Vec<Star>> {
		return read_stars_from_file(self::FILENAME);
	}
	pub fn generate_angle(stars: &Vec<Star>) -> Vec<super::Identification::AnglePair> {
		use itertools::Itertools;

		return stars.iter().enumerate().tuple_combinations().map(|(a,b)| {
			return ((a.0 as u16,b.0 as u16),Star::angle(a.1,b.1));
		}).collect();
	}
}

